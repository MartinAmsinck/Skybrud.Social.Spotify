namespace Skybrud.Social.Spotify.Options.Player {

    /// <summary>
    /// Enum class representing the repeat state of a spotify Player.
    /// </summary>
    public enum SpotifyPlayerRepeatState {

        /// <summary>
        /// Indicates that the player should repeat the current track.
        /// </summary>
        Track,

        /// <summary>
        /// Indicates that the player should repeat the current context.
        /// </summary>
        Context,

        /// <summary>
        /// Indicates that repeat should be turned off.
        /// </summary>
        Off

    }

}